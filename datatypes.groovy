class Example { 
   static void main(String[] args) { 
      //Example of a int datatype 
      int x = 5; 
        
      //Example of a long datatype 
      long y = 100L; 
        
      //Example of a floating point datatype 
      float a = 10.56f; 
        
      //Example of a double datatype 
      double b = 10.5e40; 
        
      //Example of a BigInteger datatype 
      BigInteger bi = 30g; 
        
      //Example of a BigDecimal datatype 
      BigDecimal bd = 3.5g; 
        
      println(x); 
      println(y); 
      println(a); 
      println(b); 
      println(bi); 
      println(bd); 

      String q = "Jaffa"
      println(q)

      int m = 5
      int n = 2
      println("Value of m is "+ m + " Value of n is "+ n)
      println(2**3)

      // Range operators
      def l = 9..20
      println(l);
      println(l.get(2))
   } 
}